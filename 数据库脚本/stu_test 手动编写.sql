create table admin_account(
	admin_username char(20) primary key,
    admin_password char(20) not null
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table id_course(
	course_id char(20) primary key,
    course_name char(20) not null
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table student_account(
	student_username char(20) primary key,
    student_password char(20) not null
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table student_info(
	student_id char(20) primary key,
    student_name char(20) not null,
    student_class char(20) ,
    student_username char(20)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table student_course(
	student_id char(20) primary key,
    course_id char(20)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table teacher_account(
	teacher_username char(20) primary key,
    teacher_password char(20) not null
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table teacher_info(
	teacher_id char(20) primary key,
    teacher_name char(20) not null,
    teacher_username char(20)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table teacher_course(
	teacher_id char(20) primary key,
    course_id char(20)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table questions(
	question_no char(20),
    teacher_id char(20),
    course_id char(20),
    question_describe char(200) ,
    question_choice_A char(100) ,
    question_choice_B char(100) ,
    question_choice_C char(100) ,
    question_choice_D char(100) ,
	if_keynote bool,
    difficulty_degree int,
    question_correct_choice char(1) ,
    question_answer_detail char(200) ,
    primary key(question_no,teacher_id,course_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table do_question(
	student_id char(20) ,
    question_no char(20) ,
    teacher_id char(20),
    course_id char(20),
    question_do_time datetime ,
    if_correct bool ,
    primary key(student_id,question_no,question_do_time,teacher_id,course_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table notice_teacher(
	admin_username char(20) primary key,
    notice_contents char(200),
	notice_time datetime
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table notice_student(
	admin_username char(20) primary key,
    notice_contents char(200),
	notice_time datetime
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

create table messages(
	student_id char(20),
	teacher_id char(20),
	message_time datetime,
	message_contents char(200),
	primary key(student_id,teacher_id,message_time)
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_general_ci;

